let create_token lexbuf =
  let str = lexeme lexbuf in
  match List.assoc_opt str reserved_words with
  | None   -> LID str
  | Some t -> t

let create_symbol lexbuf =
  let str = lexeme lexbuf in
  match List.assoc_opt str symbols with
  | None   -> raise (Lexer_error ("Unexpected token: " ^ str))
  | Some t -> t

